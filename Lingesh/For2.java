public class For2 
{
    public static void main(String... args)
    {
        System.out.print("Enter the value in prallel series ");
        for(int i=1;i<=10;i++)
        {
            for(int j=1;j<=10;j--)
            {
                System.out.println(i);
                System.out.println(j);
            }
        }
    }
}
